#include <bits/stdc++.h>

using namespace std;
// https://codeforces.com/problemset/problem/1985/B
// O(t)
int main () {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        
        if (n == 3) {
            cout << 3 << endl;
        } else {
            cout << 2 << endl;
        }
    }
}